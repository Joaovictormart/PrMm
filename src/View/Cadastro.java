    private void Cadastrar() {

        String Nome = Nome_usuario.getText();
        String User = UserLogin.getText();
        String Senha = Pass1.getText();

        if (Senha.equals(Pass2.getText())) {

            DadosUsuario BDuser = new DadosUsuario();
            
            BDuser.setNome_usuario(Nome);
            BDuser.setUser(User);
            BDuser.setSenha(Senha);
            
        } else {
            JOptionPane.showMessageDialog(null, "As senhas digitadas são diferentes!");
        }

    }

